def add_picture(doc: Document, path: Path, caption: str, width_cm=16.0) -> None:
    if not path.exists():
        return
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(6)
    p.add_run().add_picture(str(path), width=Cm(width_cm))
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.space_before = Pt(0)
    cap.paragraph_format.space_after = Pt(6)
    cap.paragraph_format.line_spacing = 1.0
    run = cap.add_run(caption)
    run.font.name = BODY_FONT
    run.font.size = Pt(12)
    run.italic = True


def add_table(doc: Document, headers: list[str], rows: list[list[str]], widths: list[float]):
    table = doc.add_table(rows=1, cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    for row in table.rows:
        for i, width in enumerate(widths):
            row.cells[i].width = Cm(width)
    hdr = table.rows[0].cells
    for i, head in enumerate(headers):
        hdr[i].vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER
        p = hdr[i].paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.line_spacing = 1.0
        p.add_run(head).bold = True
        set_cell_border(hdr[i])
    for row_values in rows:
        cells = table.add_row().cells
        for i, value in enumerate(row_values):
            cells[i].vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.TOP
            p = cells[i].paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            p.paragraph_format.line_spacing = 1.0
            p.paragraph_format.space_before = Pt(0)
            p.paragraph_format.space_after = Pt(0)
            r = p.add_run(value)
            r.font.name = BODY_FONT
            r.font.size = Pt(12)
            set_cell_border(cells[i])
    doc.add_paragraph()


def add_toc(doc: Document, page_map: dict[str, int] | None = None) -> None:
    add_heading(doc, "Содержание", level=1, page_break=False)
    for item in TOC_ITEMS:
        p = doc.add_paragraph()
        p.paragraph_format.first_line_indent = None
        p.paragraph_format.left_indent = None
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after = Pt(0)
        p.paragraph_format.line_spacing = 1.5
        tabs = p.paragraph_format.tab_stops
        tabs.clear_all()
        tabs.add_tab_stop(Cm(16), WD_TAB_ALIGNMENT.RIGHT)
        title_run = p.add_run(item)
        title_run.font.name = BODY_FONT
        title_run.font.size = Pt(13)
        page = str(page_map.get(item, "")) if page_map else ""
        page_run = p.add_run("\t" + page)
        page_run.font.name = BODY_FONT
        page_run.font.size = Pt(13)
    doc.add_page_break()


